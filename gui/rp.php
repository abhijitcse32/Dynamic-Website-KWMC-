    <img src="images/Page-1.jpg" width="367" height="539"/> <h1>Biography of R P Shaha</h1>
    <div style="width:580px; float:right; overflow:hidden; font-size:13px; text-align:justify">
		Rai Bahadur Ranada Prasad Shaha was a true son of this soil, a man extraordinaire,
		whose life’s work and achievements are beyond comparison. Not only did he 
		sacrifice his life for his beloved country but his legacy - Kumudini Welfare 
		Trust of Bengal (KWT) - stands as the highest form of philanthropy. The Trust 
		was founded in June 1947 with the mission of providing exclusive service to the 
		suffering humanity.
		<br><br>
		
		A great visionary, he was indeed concerned about the well being of the poor and 
		specially the underprivileged women. He bequeathed all his wealth for the 
		welfare of the less fortunate. What prompted him to undertake this initiative 
		was his childhood experience of his mother’s death and his love for humanity. 
		R P Shaha was born in 1896 to Debendra Kumar and Srimati Kumudini. He was only 
		seven years old when his mother died a painful death from tetanus infection.
		<br><br>
		
		The death of his mother left a profound impact on the young mind of R P Shaha. 
		He made a promise that if his fate permitted, he would do something for the 
		ailing mothers and women. After his mother’s death, his father remarried 
		leaving him restless and yearning for motherly care and attention. In this 
		situation he was sent to Kishorgonj to stay with his maternal uncle. He spent 
		his adolescence there, where his formal education could not go beyond the first 
		few years in elementary school. Young R P Shaha ran away from home and began a 
		new life in Calcutta selling newspapers at Howrah Railway Station and working 
		as a canteen boy. He also joined the Swadeshi Movement against the British 
		occupation and that landed him in jail for some time.
		<br><br>
		
		When the First World War broke out, he enlisted in the Bengal Ambulance 
		Corps of the British Indian Army as a soldier and was sent to Mesopotamia 
		in present day Iraq. There he performed his duties with distinction earning 
		a medal and citation from King George the Fifth. He also received a gallantry 
		award for saving some British officers from fire in their camps. Later on he was 
		awarded the Viceroy’s Commission.
		<br><br>
		
		Returning to Calcutta he took up trading in coal, starting with door-to-door 
		coal supply and gradually progressed to larger supply orders. Thus a wealthy 
		Ranada Prasad Shaha was born through sheer hard work and perseverance. In 1933 
		he became a share holder in a joint river transportation business named 
		“Bengal River Service”. Later on he bought the firm. This was the core business 
		of all his commercial ventures. By 1945, R P Shaha had already acquired the 
		entire jute business of the Scottish firm George Henderson & Company at 
		Narayanganj which was previously owned by an English firm named David & 
		Company.
		<br><br>
		
		In 1938, R P Shaha set up a dispensary with outdoor facilities at his 
		village home. It was named after his grandmother Srimati Shova Sundari. 
		On July 27, 1944 the then Governor of Bengal, Lord Richard G Casey inaugurated 
		a 750 bed hospital in Mirzapur. This free hospital was named Kumudini Hospital. 
		R.P. Shaha strongly believed that the foremost requirement to achieve women’s 
		freedom and establish their rights was healthcare and education. He 
		established an impressive institution Bharateswari Homes for girls at Mirzapur. 
		He also founded Kumudini Girls’ College at Tangail and Debendra College at 
		Manikganj.
		<br><br>
		
		In July 1944 he donated an amount of Rs. 2,50,000/- to the British 
		Red Cross Appeal Fund. In the early fifties he financed construction of 
		the Maternity Ward of Combined Military Hospital (CMH) in Dhaka.
		<br><br>
		
		This great philanthropist was abducted along with his only able son 
		Bhabani Prasad Shaha by the Pakistan Army and their collaborators on May 
		7, 1971 during the Liberation War. They never returned. His spirit lives among 
		his successors who are on the Board of Directors of the Trust. R P Shah was the 
		first Managing Director of the Trust succeeded by his daughter Joya Pati. His 
		grandson Rajiv Prasad Shaha is the present Managing Director of the Trust and so 
		the tradition goes on.
		<br><br>
		
		Huseyn Shaheed Suhrawardy, the former Prime Minister of Pakistan on a visit to 
		Kumudini Complex at Mirzapur in the mid fifties wrote in the visitor’s book:
		
		<br>
		<i><b>“A poor man became a millionaire, and the millionaire voluntarily became a poor 
		man, spending his all in the service of humanity, for the suffering and the distressed, 
		for the furtherance of education, for rendering a service to the State, which the 
		state itself has not undertaken. But is the Rai Bahadur poor; he is rich in the esteem, 
		in the affection, in the love of a grateful people; having given all his worldly 
		possessions, he has obtained more than those who were his compeers. May that 
		state and the people he has served so well give him that recognition which is 
		his due, and not destroy the great institution he has built with such love and 
		devotion.”</b></i>

