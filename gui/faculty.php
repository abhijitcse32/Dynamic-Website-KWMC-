<h1>Award of MBBS degree:</h1>
<p style="font-size:13px; text-align:justify">
KWMC is affiliated with the University of Dhaka. Faculty of Medicine of Dhaka 
University conducts the MBBS course in accordance with the curriculum laid down by 
the Bangladesh Medical and Dental Council. On successful completion of five years of 
study, MBBS degree is awarded by the University of Dhaka.
</p>
<ul style="font-size:13px; text-align:justify">
<li>MBBS course is of 5 years duration plus 1 year internship training</li>
<li>There are 4 professional examinations during the MBBS course</li>
</ul>
<p style="font-size:13px; text-align:justify">The professional examinations are held regularly as per the following schedule:<br />
1st Professional MBBS – at the end of one and a half year<br />
2nd Professional MBBS – at the end of another year<br />
3rd Professional MBBS – at the end of another one year<br />
Final Professional MBBS – at the end of another one and a half year
</p>
<br/>
<h1>Teaching Methods and Material:</h1>
<p style="font-size:13px; text-align:justify">
Theoretical lectures are delivered in the lecture hall equipped with Multimedia 
Projectors . Tutorial classes and small group discussions are held in tutorial rooms 
divided into batches. Practical/ Dissection and Histology classes are conducted batch 
wise in concerned rooms/ labs furnished with necessary instruments and cadavers. There 
are also arrangements for clinical classes in the indoor and outpatient departments in 
the hospital.
