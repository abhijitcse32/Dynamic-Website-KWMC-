     <p><u><strong>Eligibility for Admission:</strong></u><br />
       <br />
       Candidates who have passed SSC and HSC examination in science group with  physics, chemistry and Biology from any recognized Board/ University of Bangladesh  with a total grade point average of 8.00 by both the examinations. But, the  grade average for each of the exams must not be less than 3.50 and in HSC grade  point in Biology must not be less than 3.00. Students having O level and A  level qualifications should apply with certificate of equivalence issued from  the competent authority of Director General of Health service, Govt. of the  peoples’ republic   of Bangladesh. The  students who passed SSC or equivalent examination 02 sessions before are not  eligible to apply for the admission test. Married students are not eligible to  apply for admission. </p>
